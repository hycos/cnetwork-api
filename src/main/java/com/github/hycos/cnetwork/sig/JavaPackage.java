package com.github.hycos.cnetwork.sig;

public class JavaPackage {

    public static final JavaPackage DEFAULT = new JavaPackage("(default package)");

    private String packageName;

    public JavaPackage(String packageName) {
        this.packageName = packageName;
    }

    public String getName() {
        return packageName;
    }

    /* (non-Javadoc)
     * @see java.lang.Object#hashCode()
     */
    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result
                + ((packageName == null) ? 0 : packageName.hashCode());
        return result;
    }

    /* (non-Javadoc)
     * @see java.lang.Object#equals(java.lang.Object)
     */
    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (!(obj instanceof JavaPackage)) {
            return false;
        }
        JavaPackage other = (JavaPackage) obj;
        if (packageName == null) {
            if (other.packageName != null) {
                return false;
            }
        } else if (!packageName.equals(other.packageName)) {
            return false;
        }
        return true;
    }
}